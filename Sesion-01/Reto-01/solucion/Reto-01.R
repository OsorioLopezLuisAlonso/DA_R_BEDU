#Area de un Rombo

diagonalMenor <- 20
diagonalMayor <- 30
area.Rombo <- (diagonalMenor*diagonalMayor)/2
area.Rombo

#Area de un Cubo

arista.Cubo <- 16
area.Cubo <- 6*(arista.Cubo^2)
area.Cubo

#Area de un Tetraedo
arista.tetraedro <- 1
area.tetraedro <- (3^(1/2))*(arista^2) 
area.tetraedro

#Area de un Cilindro
radio.Cilindro <- 15
altura.Cilindro <- 10
pi <- 3.1416
area.Cilindro <- (2 * pi * radio.Cilindro * (radio.Cilindro + altura.Cilindro))
area.Cilindro

