#Area de un Rectangulo

base.rectangulo <- 10
altura.rectangulo <- 2
area.rectangulo <- base.rectangulo * altura.rectangulo
area.rectangulo

#Area de un Triangulo

base.triangulo <- 10
altura.triangulo <- 2
area.triangulo <- (base.triangulo * altura.triangulo) / 2
area.triangulo

#Area de un Circulo

radio <- 10
pi <- 3.1416
area.circulo<- (pi * radio^2)
area.circulo

#Area de un Trapecio

baseMenor <- 15
baseMayor <- 30
altura.trapecio <- 10
area.trapecio<- ((baseMayor+baseMenor)/2)*altura.trapecio
area.trapecio

